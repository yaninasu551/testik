function removeGifts(){
    const all_gifts = document.querySelectorAll(".gift");
    all_gifts.forEach((el) => {
            el.remove()
    })
}

function findGifts() {
    removeGifts();

    const select = document.querySelectorAll("select");
    let i = 0;
    for (const el of select) {
        const gifts = gifts_list[el.id][el.value];
        for (const gift of Object.keys(gifts)){
            i += 1;
            const img_link = gifts[gift];
            let card = document.createElement('div');
            card.classList.add('gift');
            let img = document.createElement('img');
            img.setAttribute('src', img_link);
            img.setAttribute('alt', gift);
            card.appendChild(img);
            let text = document.createElement('span');
            text.innerHTML = gift;
            card.appendChild(text);
            document.getElementById('giftList').appendChild(card);
            setInterval(()=> {
                card.classList.add('open')
            }, i * 100)
        }
    }
}
